package com.kodilla.testing.calculator;

public class Calculator {

    private int a = 0;
    private int b = 0;

    public Calculator(){
        this.a = a;
        this.b = b;
    }

    public int add(int a, int b){
     return a + b;
    }

    public int sub(int a, int b){
        return a - b;
    }

    public int getA(){
        return a;
    }

    public int getB() {
        return b;
    }
}
